hook.Add("CanPlayerSpeak", "ix.RestrictFactionChat", function(client, chatType)
    local char = client:GetCharacter()
    if not char then return end

    if char:GetData("chatRestricted") then
        if chatType == "ic" or chatType == "w" or chatType == "y" then
            client:Notify("You are restricted from speaking in this way.")
            return false
        end
    end
end)


function PLUGIN:SetupActs()
	-- sit
	ix.act.Register("Console", {"stalker"}, {
		start = {"console_work_pre"},
		sequence = {"console_work_looping"},
		finish = {"console_work_post"},
		}
	)
end

function PLUGIN:PlayerFootstep(client, position, foot, soundName, volume)
	local factionTable = ix.faction.Get(client:Team());

	if (factionTable.walkSounds) then
		client:EmitSound(factionTable.walkSounds[foot]);
		return true;
	end
end

